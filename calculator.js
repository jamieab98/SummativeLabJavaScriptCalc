function addition(a, b) {
    let sum = a + b;
    console.log(a, "+", b, "=", sum)
}
addition(2, 3)

function subtraction(a, b) {
    let difference = a - b;
    console.log(a, "-", b, "=", difference)
}
subtraction(2, 3)

function multiply(a, b) {
    let product = a * b;
    console.log(a, "*", b, "=", product)
}
multiply(2, 3)

function divide(a, b) {
    let quotient = a / b;
    console.log(a, "/", b, "=", quotient)
}
divide(2, 3)